using Microsoft.VisualBasic;
using System;

namespace LoopsIntro
{
    internal class Program
    {
        static void Main(string[] args)
        {
            for (int counter = -3; counter < 4; counter++)
            {
                Console.WriteLine(counter);
            }
            Console.WriteLine("For loop is done");
            Console.Read();            
        }
    }
}





